<?php
    function soma($numero1, $numero2) {
        $res = $numero1 + $numero2;
        return $res;
    }
    
    function subtracao($numero1, $numero2) {
        $res = $numero1 - $numero2;
        return $res;
    }
    
    function multiplicacao($numero1, $numero2) {
        $res = $numero1 * $numero2;
        return $res;
    }
    
    function divisao($numero1, $numero2) {
        $res = $numero1 / $numero2;
        return $res;
    }
    

?>

