            <h2 class="titulo1">III FHISTART - Educação Inclusiva – Direito à Diversidade</h2> 
            <p>
                Nessa terceira versão do evento pretende-se trabalhar de forma sensibilizadora às demandas inclusivas na formação do educador, desenvolver um conhecimento teórico e prático sobre a importância da inclusão educacional dos alunos com déficits nos aspectos físico, mental e psicossocial através de propostas didático-pedagógicas inovadoras, situando-se como sujeitos corresponsáveis pela inserção social.
            </p>
            <p>
                O referido evento tem data prevista para o dia 04 de julho/2013, com a participação de uma equipe por turma que apresentará dentro de três modalidades: exposição temática, produção de vídeo e apresentação musical.
            </p>
            <p>
                Serão premiadas as três turmas que mais somarem pontos. A primeira colocada receberá um troféu e uma quantia de R$ 500,00 (quinhentos reais); a segunda, um troféu e o valor de 300,00 (trezentos) reais e a terceira colocada receberá também o troféu e valor de R$ 200,00 (duzentos reais). As demais equipes serão contempladas com troféus pela participação.
            </p>
            <p>
                O III FHISTART é um evento aberto a toda a comunidade. A entrada será cobrado dois quilos de alimentos não perecível, para distribuição de cestas básicas às famílias carentes do Município de Araguatins.
