            <h2 class="titulo1">IV FHISTART - Políticas Públicas: Um novo olhar para a Educação</h2> 
            <p><strong>ATENÇÃO - </strong></p>
            <p>Caso seu certificado não esteja disponível ou com dados errados, favor entrar em contato, e-mail: <strong>quiteria@ifto.edu.br</strong>, assunto fhistart.</p><br><br>
           
        
			<p>
             <b>O IV FHISTART - Políticas Públicas: Um novo olhar para a Educação</b>, foi realizado conforme previsto no dia 30 de outubro de 2014 e entre acadêmicos do IFTO e convidados em geral que direta ou indiretamente contribuíram para o sucesso do evento. Nessa quarta versão, o projeto envolveu um maior número de acadêmicos que enfatizaram as Políticas Públicas com um novo olhar para a Educação.
            </p>
            <p>
                A programação do IV Festival de História e Arte (FHISTART) do campus Araguatins do Instituto Federal do Tocantins (IFTO) aconteceu no refeitório do campus iniciando  às 19h, o espaço permaneceu lotado com as respectivas turmas sempre animadas e participativas, dando um show de criatividade e competência conforme a diversidade de temas trabalhados.
            </p>
            <p>
                Cada equipe desenvolveu suas respectivas atividades de acordo a proposta do projeto na qual vinham trabalhando desde o início do ano, com apresentação de um seminário temático onde cada equipe fez sua exposição teórica,  peça teatral e uma paródia voltada aos respectivos temas.
            </p>
            <p>
                O Festival é aberto à comunidade em geral. Para participar da programação do evento, basta levar dois quilos de alimentos não perecíveis, que serão distribuídos em cestas básicas às famílias carentes do município de Araguatins.
            </p>
            <p>
                <b>RESULTADO E PREMIAÇÃO</b>
            </p>
            <p>
                Após as apresentações saiu então o esperado resultado publicado pelo idôneo e competente corpo de jurados. 
            </p>
                <center><b>CLASSIFICAÇÃO DAS 3 PRIMEIRAS EQUIPES COLOCADAS</b></center>
            <p>
				
				1º COLOCADO - 6º Período de Biologia <br>
				2º COLOCADO - 6º Período de Computação<br>
				3º COLOCADO - 4º Período de Computação
            </p>
            <p>
                A premiação foi entregue no ato de encerramento, sendo repassado troféus a todos os representantes de turma, porém as três primeiras colocadas, além do troféu receberam uma quantia em dinheiro, sendo assim distribuído: 1ª COLOCADA: 500, 2ª COLOCADA: 300,00 e a 3ª COLOCADA, o valor de 200,00.
            </p>
            <p>
                <b>AÇÃO SOLIDÁRIA</b>
            </p>
            <p>
                De acordo o regulamento a turma que ficasse como última colocada teria a responsabilidade de distribuir as cestas básicas com os alimentos arrecadados como ingresso para participação no festival.   
            </p>
            <p>
                Finalizando os trabalhos, a coordenadora do evento, Professora Quitéria Alcântara agradeceu o empenho de todos e destaca a importância do FHISTART pela contribuição aos acadêmicos, fazendo com que vejam seu papel na sociedade e responsabilidade como protagonistas desse cenário.
            </p><br>
