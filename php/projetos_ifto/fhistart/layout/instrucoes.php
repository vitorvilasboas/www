             <h2 class="titulo1">III FHISTART - Instruções para a Inscrição de Equipes</h2> 
                <p>
                    Somente os representantes de cada turma terão acesso para o cadastro da equipe participante do III FHISTART. Sendo que tal cadastramento é obrigatório para a participação da equipe no evento e para efetua-lo o representante deve seguir os seguintes passos:
                    <br><br>
                    1º - Efetuar Login no sistema utilizando o CPF como usuário e o mesmo CPF como senha;<br><br>
                    2º - Cadatrar a equipe, preenchendo os campos Professor Representante, Título do Vídeo, Título da Música e Autor da Música e, posteriormente clicando no botão "ENVIAR";<br><br>
                    3º - Cadastrar cada membro da equipe. Para isso o representante deve clicar no sinal "+", preencher os campos e clicar no botão "Adicionar Membro";<br><br>
                    4º - Aceitar os termos de compromisso marcando a caixa referente a esta opção;<br><br>
                    5º - Efetivar a inscrição clicando no botão "Ver Inscrição".<br><br>
